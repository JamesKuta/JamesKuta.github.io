Levels.prototype.draw = function(context, currentLevel)
{
    for(let i = 0; i < currentLevel.rows; i++)
    {
        for(let j = 0; j < currentLevel.columns; j++)
        {
            context.imageSmoothingEnabled = false;
            context.drawImage(images.images[currentLevel.grid[j * currentLevel.columns + i]].tile
                                , images.images[currentLevel.grid[j * currentLevel.columns + i]].x
                                , images.images[currentLevel.grid[j * currentLevel.columns + i]].y
                                , images.images[currentLevel.grid[j * currentLevel.columns + i]].width
                                , images.images[currentLevel.grid[j * currentLevel.columns + i]].height
                                , i * this.tileWidth
                                , j * this.tileHeight
                                , this.tileWidth
                                , this.tileHeight
                                ); 
        }
    }
};