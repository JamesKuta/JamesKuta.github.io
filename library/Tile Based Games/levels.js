Levels.prototype.draw = function(context, currentLevel, image)
{
    //console.log(imageArr);
    for(let i = 0; i < currentLevel.rows; i++)
    {
        for(let j = 0; j < currentLevel.columns; j++)
        {
            context.imageSmoothingEnabled = false;
            context.drawImage(image
                                , currentLevel.grid[j * currentLevel.columns + i] * 32
                                , 0
                                , 32
                                , 32
                                , i * this.tileWidth
                                , j * this.tileHeight
                                , Math.floor(this.tileWidth)
                                , Math.floor(this.tileHeight)
                                ); 
        }
    }
};